        static let primaryBlue = Color(red: 0.18, green: 0.44, blue: 0.64)
        static let accentYellow = Color(red: 0.96, green: 0.75, blue: 0.26)
        static let softBrown = Color(red: 0.58, green: 0.45, blue: 0.35)
        static let cardBackground = Color(red: 0.87, green: 0.85, blue: 0.80)
    }
}

